public class ValorGrafica {
    
    private int anio;
    private int valor;

    public ValorGrafica(int anio, int valor) {
        this.anio = anio;
        this.valor = valor;
    }

    public int getAnio() {
        return anio;
    }

    public void setAnio(int anio) {
        this.anio = anio;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }
    
    
    
    
    
}
